package com.example.demo;

public class Person {
    private String FirstName;
    private String LastName;

    public String getFirstName() {
        return FirstName;
    }
    public String getLastName() {
        return LastName;
    }

    public void setFirstName(String harrison) {
        FirstName = harrison;
    }
    public void setLastName(String garcia) {
        LastName = garcia;
    }
}
